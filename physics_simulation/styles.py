def grey_lines(self):
    noFill()
    stroke(128)
    strokeWeight(4)
    strokeJoin(ROUND);
    
def blue_lines(self):
    noFill()
    stroke(100, 100, 255)
    strokeWeight(4)
    strokeJoin(ROUND);
    
def thin_green_lines(self):
    noFill()
    stroke(100, 255, 100)
    strokeWeight(2)
    strokeJoin(ROUND);
    
def thin_yellow_lines(self):
    noFill()
    stroke(255, 255, 100)
    strokeWeight(2)
    strokeJoin(ROUND);
    
def white_fill(self):
    noStroke()
    fill(255)
    
def dark_grey_fill(self):
    noStroke()
    fill(100)
    
def magenta_fill(self):
    noStroke()
    fill(255, 100, 255)
